int add(int a, int b)
{
	return a + b;
}

int minus(int a, int b)
{
	return a - b;
}

int multi(int a, int b)
{
	return a * b;
}

float divide(float a, float b)
{
	return a / b;
}
int remain(int a, int b)
{
	return a % b;
}

	